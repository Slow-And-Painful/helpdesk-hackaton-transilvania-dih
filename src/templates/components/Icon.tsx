export type IconProps = Omit<JSX.HtmlTag, "children"> & {
  class?: string
  name: IconName
  size?: number
}

const Icon = ({ class: className, name, size = 20, ...props }: IconProps) => {
  return (
    <div
      class={["icon", className]}
      style={{
        ["--icon-size" as keyof JSX.CSSProperties]: size,
      }}
      {...props}
    >
      <svg width={size} height={size} viewBox="0 0 24 24" role="img">
        <use href={`#${name}`} />
      </svg>
    </div>
  )
}

export default Icon
